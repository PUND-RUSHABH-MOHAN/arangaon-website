// State Management
let currentLang = localStorage.getItem('lang') || 'en';

// i18n Functions
function t(key) {
  return translations[currentLang]?.[key] || translations['en']?.[key] || key;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  updatePageTranslations();
  updateLangToggleButtons();
}

function updatePageTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
}

function updateLangToggleButtons() {
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    const lang = btn.getAttribute('data-lang');
    btn.classList.toggle('active', lang === currentLang);
  });
}

// Event Listeners for Language Toggle
function initLanguageToggle() {
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.getAttribute('data-lang'));
    });
  });
  updatePageTranslations();
  updateLangToggleButtons();
}

// Initialize Committee Members Section
function initCommitteeMembers() {
  const gridContainer = document.querySelector('.grid.gap-4.sm\\:grid-cols-2.lg\\:grid-cols-3.xl\\:grid-cols-4');
  if (gridContainer) {
    gridContainer.innerHTML = committeeMembers.map((member, i) => `
      <div class="rounded-xl border bg-card p-4 shadow-[var(--shadow-card)] flex flex-col gap-1">
        <span class="font-semibold text-foreground">${member.name}</span>
        <span class="text-sm text-primary font-medium">${t(member.roleKey)}</span>
      </div>
    `).join('');
  }
}

// Initialize Gallery
function initGallery() {
  const gallerySection = document.getElementById('gallerySection');
  if (gallerySection) {
    gallerySection.innerHTML = `
      <div class="grid gap-5 sm:grid-cols-2 lg:grid-cols-3">
        ${galleryImages.map((img, i) => `
          <figure class="rounded-xl overflow-hidden border bg-card shadow-[var(--shadow-card)]">
            <img src="${img.src}" alt="${t(img.caption)}" class="w-full h-56 object-cover">
            <figcaption class="p-4 text-sm text-muted-foreground">${t(img.caption)}</figcaption>
          </figure>
        `).join('')}
      </div>
    `;
  }
}

// Contact Form Handler
function initContactForm() {
  const form = document.getElementById('contactForm');
  if (form) {
    form.addEventListener('submit', (e) => {
      e.preventDefault();
      const name = document.getElementById('contactName')?.value || '';
      const msg = document.getElementById('contactMessage')?.value || '';
      const subject = t('contact.subjectPrefix') + (name || t('contact.website'));
      const mailto = `mailto:grampanchayat@arangaon.org?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(msg)}`;
      window.location.href = mailto;
    });
  }
}

// Initialize Navigation Active State
function initNavigation() {
  const currentPage = window.location.pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('nav a').forEach(link => {
    const href = link.getAttribute('href');
    if (href === currentPage) {
      link.classList.add('active');
    } else {
      link.classList.remove('active');
    }
  });
}

// Master Init Function
document.addEventListener('DOMContentLoaded', () => {
  initLanguageToggle();
  initCommitteeMembers();
  initGallery();
  initContactForm();
  initNavigation();
});
